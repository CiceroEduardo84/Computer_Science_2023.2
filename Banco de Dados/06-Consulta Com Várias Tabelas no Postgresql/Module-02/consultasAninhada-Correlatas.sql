SELECT CODIGOFUNCIONARIO,NOME,SALARIO
FROM FUNCIONARIO F
 	 JOIN (
		 SELECT CODIGODEPARTAMENTO,AVG(SALARIO) AS MEDIA
		 FROM FUNCIONARIO
		 GROUP BY CODIGODEPARTAMENTO) TESTE
	ON F.CODIGODEPARTAMENTO=TESTE.CODIGODEPARTAMENTO
WHERE SALARIO>MEDIA;

