SELECT CODIGOFUNCIONARIO, NOME, SALARIO
FROM FUNCIONARIO
WHERE SALARIO < (
	
	SELECT AVG(SALARIO)
	FROM FUNCIONARIO F JOIN DEPARTAMENTO D ON 
	(F.CODIGODEPARTAMENTO=D.CODIGODEPARTAMENTO)
	WHERE D.NOME = 'Tecnologia da Informação');
	
