FROM FUNCIONARIO
WHERE SALARIO < (
	
	SELECT AVG(SALARIO)
	FROM FUNCIONARIO
	WHERE CODIGODEPARTAMENTO IN (
		
		SELECT CODIGODEPARTAMENTO
		FROM DEPARTAMENTO
		WHERE NOME='Tecnologia da Informação'
